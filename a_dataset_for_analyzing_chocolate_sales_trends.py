# Contar o número de vendas por mês (mesmo que a gente já tenha o gráfico lá em cima, quero ver os números)
dfClean["TotalSales"] = dfClean.groupby("YearMonth")["Sales Person"].transform("count")

# Verificar resultado
dfClean[["YearMonth", "TotalSales"]].drop_duplicates()

"""## Vamos ver se daria para trbalhar com ML nessa base:

Verificarei Tendências Sazonais 🔍   
Mas para isso, eu preciso separar minha base em hemisférios, já que tenho países que trabalham com épocas diferentes para as estações do ano

Hemisfério Sul (Austrália, Nova Zelândia)

As estações:

* Primavera: setembro a dezembro  
* Verão: dezembro a março  
* Outono: março a junho  
* Inverno: junho a setembro
"""

dfSul = dfClean.copy()

# Criar uma função para classificar a estação do ano
def get_season(month):
    if month in [12, 1, 2]:
        return "Verão"
    elif month in [3, 4, 5]:
        return "Outono"
    elif month in [6, 7, 8]:
        return "Inverno"
    else:
        return "Primavera"

# Criar nova coluna "Season" com a estação correspondente
dfSul["Season"] = dfSul["Date"].dt.month.apply(get_season)

# Verificar resultado
dfSul.head()

# Agrupar as vendas totais por estação do ano
seasonSalesSul = dfSul.groupby("Season")["TotalSales"].sum().reset_index()

# Criar gráfico de barras
plt.figure(figsize=(8, 5))
sns.barplot(x="Season", y="TotalSales", hue="Season", data=seasonSalesSul, palette=custom_palette)

# Ajustes no gráfico
plt.title("Total de Vendas por Estação do Ano")
plt.xlabel("Estação do Ano")
plt.ylabel("Total de Vendas")
plt.xticks(rotation=45)
plt.show()

