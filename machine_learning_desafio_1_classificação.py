# Definições de valores possíveis para cada coluna categórica
culturas = ["Arroz", "Milho", "Café", "Trigo", "Soja"]
regioes = ["Norte", "Nordeste", "Centro-Oeste", "Sudeste", "Sul"]
tipos_solo = ["Argiloso", "Arenoso", "Misto", "Siltoso"]
tipos_irrigacao = ["Irrigado", "Seca"]
tipos_fertilizante = ["Químico", "Orgânico", "Nenhum"]

# Função para simular produtividade baseada em características
def gerar_produtividade(cultura, irrigacao, fertilizante):
    base = random.uniform(30, 100)
    if irrigacao == "Seca":
        base -= random.uniform(10, 30)
    if fertilizante == "Nenhum":
        base -= random.uniform(5, 15)
    elif fertilizante == "Orgânico":
        base += random.uniform(0, 5)
    else:
        base += random.uniform(5, 10)
    return round(max(10, min(base, 100)), 2)

# Função para definir status de produção baseado na produtividade
def definir_status(produtividade):
    return "Alta" if produtividade >= 70 else "Baixa"

# Gerar os dados sintéticos
random.seed(42)
np.random.seed(42)
linhas = 5000000
dados = []

