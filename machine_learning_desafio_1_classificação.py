    fertilizante = random.choice(tipos_fertilizante)
    area = round(random.uniform(1, 100), 2)
    produtividade = gerar_produtividade(cultura, irrigacao, fertilizante)
    status = definir_status(produtividade)
    dados.append([cultura, regiao, solo, irrigacao, fertilizante, area, produtividade, status])

