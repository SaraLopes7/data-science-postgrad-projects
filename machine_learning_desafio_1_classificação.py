import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.ensemble import RandomForestClassifier
from sklearn.preprocessing import OneHotEncoder
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.linear_model import LogisticRegression
from sklearn.svm import SVC
from sklearn.metrics import classification_report, confusion_matrix, ConfusionMatrixDisplay, roc_curve, roc_auc_score, accuracy_score
import matplotlib.pyplot as plt
from sklearn.model_selection import cross_val_score
import seaborn as sns
import random

df = pd.read_csv('/content/dataset_agrario_pratica.csv')

df.head()

print(df.isnull().sum())

df.shape

# Variáveis categóricas
cat_cols = ['Cultura', 'Região', 'Tipo de Solo', 'Tipo de Irrigação', 'Tipo de Fertilizante']
num_cols = ['Área (ha)', 'Produtividade (sacas/ha)']

# Separar X e y
X = df.drop('Status de Produção', axis=1)
y = df['Status de Produção']

# Separar treino e teste
X_train, X_test, y_train, y_test = train_test_split(X, y, stratify=y, test_size=0.3)

# Pré-processamento com OneHotEncoder
preprocessor = ColumnTransformer(
    transformers=[
        ('cat', OneHotEncoder(handle_unknown='ignore'), cat_cols)
    ],
    remainder='passthrough'
)

# Visulização dos DFs gerados:
X.head()
y.head()

y.shape

"""## **🤖 Modelos testados:**

### **😠 Regressão Logística**
"""

pipe_lr = Pipeline(steps=[
    ('preprocessor', preprocessor),
    ('classifier', LogisticRegression(max_iter=1000))
])

pipe_lr.fit(X_train, y_train)
y_pred_lr = pipe_lr.predict(X_test)
print("Logistic Regression:\n", classification_report(y_test, y_pred_lr))

pred = pipe_lr.predict(X)
print(f'Previsão:  {pred[0]}')

y_real = df['Status de Produção'].values  # verdadeiro, extraído do dataframe original

# Ver relatório de desempenho
print("Acurácia:", accuracy_score(y_real, pred))
print("\nRelatório completo:\n", classification_report(y_real, pred))

# Matriz de confusão
cm = confusion_matrix(y_real, pred, labels=["Alta", "Baixa"])
disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=["Alta", "Baixa"])
disp.plot()



"""### **🌳 Random Forest**"""

pipe_rf = Pipeline(steps=[
    ('preprocessor', preprocessor),
    ('classifier', RandomForestClassifier())
])

pipe_rf.fit(X_train, y_train)
y_pred_rf = pipe_rf.predict(X_test)
print("Random Forest:\n", classification_report(y_test, y_pred_rf))

pred = pipe_rf.predict(X)
print(f'Previsão: {pred[0]}')

y_real = df['Status de Produção'].values  # verdadeiro, extraído do dataframe original

