dt.isnull().sum()

"""#### Conhecendo a amostra e nossos passageiros!"""

#Quantos homens e mulheres?
qty_male = dt['Gender'].value_counts()['Male']
qty_female = dt['Gender'].value_counts()['Female']

#Qual a média de idade e a faixa etária dessa amostra?
media_idade = dt['Age'].mean()
under_18 = (dt['Age']<18).sum()
under_30 = ((dt['Age']>=18) & (dt['Age']<30)).sum()
under_50 = ((dt['Age']>=30) & (dt['Age']<50)).sum()
upper_50 = (dt['Age']>=50).sum()

#Quantos são passageiros recorrente (fiel) e quantos não são?
loyal_pass = dt['Customer Type'].value_counts()['Loyal Customer']
disloyal_pass = dt['Customer Type'].value_counts()['disloyal Customer']

#Quantos passageiros andam em cada classe?
bus_class = dt['Class'].value_counts()['Business']
eco_class = dt['Class'].value_counts()['Eco']
eco_plus = dt['Class'].value_counts()['Eco Plus']

#Qual é o tipo de viagem que o nosso cliente costuma fazer? Pessoal ou a negócios?
business_travel = dt['Type of Travel'].value_counts()['Business travel']
personal_travel =  dt['Type of Travel'].value_counts()['Personal Travel']

#REPORT (perfil do usuário do serviço)

