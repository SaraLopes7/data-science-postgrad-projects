print("\nEntre todos os passageiros,", loyal_pass,"são passageiros leais/recorrentes e", disloyal_pass,"não são.")
print("\nSeparando por classe de viagem:\n-",bus_class,"passageiros viajam na Business;\n-",eco_plus,"passageiros viajam na Eco Plus;\n-",eco_class,"passageiros viajam na Eco.")
print("\nDividindo por tipo de viagem: \n-",business_travel, "passageiros viajam a negócios;\n-",personal_travel, "passageiros fazem viagens pessoais.")

"""#### Analisando as respostas da avaliação (notas de 0 a 5) sobre a satisfação dos clientes"""

#Verificando a média de cada categoria avaliada
media_notas = round(dt.mean(numeric_only=True),2).to_frame()
media_notas

#Separando as categorias de satisfação da amostra:
satisfaction = media_notas <= 5
satisfaction_categories = media_notas[satisfaction]
print('As categorias de satisfação do cliente são:')
satisfaction_categories.dropna()

#Considerando que abaixo de 3 a nota seja ruim, vamos separar os aspectos que podem representar fraquezas da empresa ou pontos que podem melhorar:
aspectos_negativos = media_notas < 3
negativos = media_notas[aspectos_negativos]
print('Os aspectos avaliados pelos passageiros como negativos da companhia são:')
negativos.dropna()

#Quem são os clientes que votaram abaixo de 3 para o pior item avaliado?
classe_internet = dt[dt['Inflight wifi service'] < 3].groupby(['Class', 'Type of Travel']).size().to_frame(name='Reclamações')
print("Os viajantes a trabalho da classe Business tiveram um nível de reclamação superior a qualquer outra classe e/ou tipo de viajante.\
\nNesse caso é importante que a empresa melhore sua disponibilidade de internet com o objeitivo de manter a fidelidade dos passageiros, uma vez que o valor das passagens são elevados.")
classe_internet

#Em que classe os consumidores são mais fieis?
dt1 = dt.groupby(['Class', 'Customer Type']).size().to_frame('Total Customers')
